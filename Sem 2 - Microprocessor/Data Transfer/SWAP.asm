MOV A, M      ; A = VAL(08A0H) "= 30H"
INX H   			; ADDRESS = 08A1H
MOV B, M	  	; B = VAL(08A1H) "= 20H"
MOV M, A	    ; VAL(08A1H) = A "= 30H"
DCX H		      ; ADDRESS = 08A0H
MOV M, B      ; VAL(08A0H) = B "= 20H"
HLT           ; "OUTPUT = 20H 30H"